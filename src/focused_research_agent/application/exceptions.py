"""重点研究代理的共享应用程序层例外。

该模块定义了代表预期应用程序/用例的异常
失败。这些异常与传输无关，可以通过以下方式处理
不同的入口点，例如 CLI、FastAPI 或未来的 UI 层。

从架构上来说，异常类属于应用层，因为
它代表业务/用例含义，而不是 HTTP、终端或 UI 行为。"""


class ApplicationError(Exception):
    """表示预期的应用程序/用例失败。

    该异常是在研究使用时由应用层引发的
    由于预期输入或业务级别，案例无法继续进行
    问题。

    参数：
        消息：应用程序错误的人类可读描述。"""

    def __init__(self, message: str) -> None:
        super().__init__(message)
